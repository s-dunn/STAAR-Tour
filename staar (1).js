// accordion function for indigo highlights (state of the art section), adapted from http://www.w3schools.com/w3css/w3css_accordions.asp 
function accordIndigo(id) {
    var x = document.getElementById(id);
    if (x.className.indexOf("w3-show") == -1) {
        x.className += " w3-show";
        x.previousElementSibling.className += " w3-indigo";
    } else { 
        x.className = x.className.replace(" w3-show", "");
        x.previousElementSibling.className = 
        x.previousElementSibling.className.replace(" w3-indigo", "");
    }
}

